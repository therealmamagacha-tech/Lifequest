# Définition des badges de succès
# (id, icon, clé_i18n, condition_fn sur stats dict)
BADGES_DEF = [
    ("first_blood", "target", "ach_first",   lambda s: s["total"] >= 1),
    ("streak_3",    "flame", "ach_streak3", lambda s: s["streak"] >= 3),
    ("streak_7",    "zap", "ach_streak7", lambda s: s["streak"] >= 7),
    ("veteran",     "award", "ach_veteran", lambda s: s["total"] >= 10),
    ("elite",       "star", "ach_elite",   lambda s: s["total"] >= 25),
    ("hard_1",      "skull", "ach_hard1",   lambda s: s["hard"] >= 1),
    ("hard_5",      "ghost", "ach_hard5",   lambda s: s["hard"] >= 5),
    ("xp_100",      "gem", "ach_xp100",   lambda s: s["total_xp"] >= 100),
    ("xp_500",      "sparkles", "ach_xp500",   lambda s: s["total_xp"] >= 500),
]


def check_badges(session_state):
    """Vérifie et débloque les nouveaux badges. Retourne la liste des nouveaux badges débloqués."""
    palmares = session_state.get("palmares", [])
    badges = list(session_state.get("badges", []))
    stats = {
        "total": len(palmares),
        "streak": session_state.get("streak", 0),
        "hard": sum(1 for e in palmares if e.get("difficulty") in ("difficile", "hard")),
        "total_xp": sum(e.get("xp_gained", 0) for e in palmares),
    }
    new_badges = []
    for badge_id, icon, label_key, condition in BADGES_DEF:
        if badge_id not in badges and condition(stats):
            badges.append(badge_id)
            new_badges.append((icon, label_key))
    session_state["badges"] = badges
    return new_badges
